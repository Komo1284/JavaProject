package static_;

public class Ex04 {
	public static void main(String[] args) {
		// 대표적인 static만 가지는 클래스 Math
		
		System.out.println("원주율 : " + Math.PI);
		System.out.println("자연 상수 : " + Math.E);
		
		int n = Math.abs(-5);
		System.out.println("n = " + n);
		System.out.println("절대값= " + Math.abs(5));
	}
}
